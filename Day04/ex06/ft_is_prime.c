int	ft_is_prime(int nb)
{
	int cp;
	int i;

	cp = 0;
	i = 1;
	while (i < nb)
	{
		if (nb % i == 0)
			cp++;
		i++;
	}
	if (cp == 1)
		return (1);
	return (0);
}
