void	ft_sort_integer_table(int *tab, int size)
{
	int i;
	int j;
	int temp;

	j = 1;
	while (j > 0)
	{
		j = 0;
		i = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
				j++;
			}
			i++;
		}
	}
}
